/**
 * @param {string} colors
 * @param {number[][]} edges
 * @return {number}
 */
const largestPathValue = function (colors, edges) {
  const g = Array(colors.length).fill().map(() => Array().fill([]))

  const inDegree = Array(colors.length).fill(0)

  for (const e of edges) {
    g[e[0]].push(e[1])
    inDegree[e[1]]++
  }

  const q = []

  const dp = Array(colors.length).fill().map(() => Array(26).fill(0))

  for (let i = 0; i < colors.length; i++) {
    if (inDegree[i] == 0) {
      q.push(i)
    }
  }

  let processed = 0
  let ans = 0

  while (q.length > 0) {
    processed++

    const curr = q.shift()
    const colorIndex = colors[curr].charCodeAt(0) - 'a'.charCodeAt(0)
    dp[curr][colorIndex]++
    ans = Math.max(ans, dp[curr][colorIndex])

    for (const n of g[curr]) {
      // check for every alphabet
      for (let i = 0; i < 26; i++) {
        dp[n][i] = Math.max(dp[n][i], dp[curr][i])
      }
      inDegree[n]--
      if (inDegree[n] == 0) {
        q.push(n)
      }
    }
  }
  return processed == colors.length ? ans : -1
}



console.log(~0)

// -1

// 2's compliment -> inverse all the numbers and add 1
// for a negative numbers, magnitude is calculated by 2's compliment
// ~0 ~=  111111111 where 1 is sign bit and 111111 2's compliment is 00000001 which is -1

console.log(5 << 2)

// a << b ~= a * 2 ** b

console.log(10 >> 2)

// a >> b ~= a / 2 ** b
